void main() {
  String hoy = "Lunes";
  switch(hoy){
    case "Domingo": print("Hoy es Domingo"); break;
    case "Lunes": print("Hoy es Lunes"); break;
    case "Martes": print("Hoy es Martes"); break;
    case "Miércoles": print("Hoy es Miércoles"); break;
    case "Jueves": print("Hoy es Jueves"); break;
    case "Viernes": print("Hoy es Viernes"); break;
    case "Sabado": print("Hoy es Sabado"); break;
  }
}